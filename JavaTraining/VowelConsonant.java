public class VowelConsonant {
    public static void main(String[] args) {
        
       char ch = 'z';

       if(ch == 'a' || ch == 'e' || ch == 'i'|| ch == 'o' || ch == 'u')
       System.out.println("The entered character is a VOWEL");
       else
       System.out.println("The entered character is a CONSONANT");
    }
}
